// STRUCTURE
//   BS_CALENDAR_DATA[year] → readonly tuple of 12 numbers
//   Each number is the day-count for that BS month (Baisakh…Chaitra).
//
// ACCURACY
//   Values are cross-referenced against:
//     • Government of Nepal official Panchanga publications
//     • Bikram Sambat calendar research papers
//     • Multiple open-source BS date libraries (averaged / verified)
//
// CONVERSION ANCHOR
//   1 Baisakh 2000 BS  ≡  14 April 1943 AD  (UTC midnight)
//   All BS↔AD arithmetic is expressed as a signed day offset from
//   this anchor, walking the table forward or backward as needed.
//
// SUPPORTED RANGE
//   BS  1970–2100  (≈ AD 1913–2044)
// =============================================================================

